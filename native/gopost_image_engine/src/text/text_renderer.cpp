static void get_text_dimensions(const GopostTextConfig* config, int32_t max_width,
                                int32_t* out_w, int32_t* out_h) {
    if (!config || !config->text || !out_w || !out_h) return;

    float line_height_mult = (config->line_height > 0.0f) ? config->line_height : 1.0f;
    float scale = (config->font_size > 0.0f) ? config->font_size : 1.0f;
    int glyph_w = (int)(kGlyphW * scale + 0.5f);
    int glyph_h = (int)(kGlyphH * scale + 0.5f);
    int line_h = (int)(glyph_h * line_height_mult + 0.5f);
    float letter_spacing = config->letter_spacing;

    int wrap_width = (max_width > 0) ? max_width : 65536;
    int x = 0, y = 0;
    int max_x = 0;
    const char* p = config->text;

    while (*p) {
        unsigned char c = (unsigned char)*p++;
        if (c < 32 || c > 126) c = 32;
        int advance = glyph_w + (int)letter_spacing;

        if (c == '\n') {
            x = 0;
            y += line_h;
            continue;
        }

        if (wrap_width > 0 && x > 0 && x + advance > wrap_width) {
            x = 0;
            y += line_h;
        }

        x += advance;
        if (x > max_x) max_x = x;
    }
    y += line_h;

    *out_w = max_x;
    *out_h = y;
}

static void blend_pixel(uint8_t* dst, int dx, int dy, int stride,
                        float r, float g, float b, float a) {
    if (a <= 0.0f) return;
    int off = dy * (int)stride + dx * 4;
    if (off < 0) return;
    float da = dst[off + 3] / 255.0f;
    float sa = a;
    float out_a = sa + da * (1.0f - sa);
    if (out_a < 1e-6f) return;
    float inv = 1.0f / out_a;
    dst[off + 0] = (uint8_t)((r * sa + (dst[off + 0] / 255.0f) * da * (1.0f - sa)) * inv * 255.0f + 0.5f);
    dst[off + 1] = (uint8_t)((g * sa + (dst[off + 1] / 255.0f) * da * (1.0f - sa)) * inv * 255.0f + 0.5f);
    dst[off + 2] = (uint8_t)((b * sa + (dst[off + 2] / 255.0f) * da * (1.0f - sa)) * inv * 255.0f + 0.5f);
    dst[off + 3] = (uint8_t)(out_a * 255.0f + 0.5f);
}

static void draw_glyph(uint8_t* dst, int dst_w, int dst_h, size_t stride,
                       int base_x, int base_y,
                       int glyph_idx, float scale,
                       float r, float g, float b, float a) {
    if (glyph_idx < 0 || glyph_idx >= 95) return;
    int sw = (int)(kGlyphW * scale + 0.5f);
    int sh = (int)(kGlyphH * scale + 0.5f);
    if (sw <= 0 || sh <= 0) return;

    const uint8_t* glyph = kBuiltinFont8x16[glyph_idx];
    for (int dy = 0; dy < sh; dy++) {
        int sy = (dy * kGlyphH) / sh;
        if (sy >= kGlyphH) sy = kGlyphH - 1;
        uint8_t row = glyph[sy];
        for (int dx = 0; dx < sw; dx++) {
            int sx = (dx * kGlyphW) / sw;
            if (sx >= kGlyphW) sx = kGlyphW - 1;
            int bit = 7 - sx;
            if ((row >> bit) & 1) {
                int px = base_x + dx;
                int py = base_y + dy;
                if (px >= 0 && px < dst_w && py >= 0 && py < dst_h) {
                    blend_pixel(dst, px, py, stride, r, g, b, a);
                }
            }
        }
    }
}

