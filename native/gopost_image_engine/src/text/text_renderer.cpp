GopostError gopost_text_init(GopostEngine* engine) {
    if (!engine) return GOPOST_ERROR_INVALID_ARGUMENT;
    std::lock_guard<std::mutex> lock(g_font_mutex);
    if (g_text_initialized) return GOPOST_OK;
    g_font_families.push_back({"Builtin", "Normal"});
    g_text_initialized = true;
    return GOPOST_OK;
}

GopostError gopost_text_shutdown(GopostEngine* engine) {
    (void)engine;
    std::lock_guard<std::mutex> lock(g_font_mutex);
    g_font_families.clear();
    g_text_initialized = false;
    return GOPOST_OK;
}

GopostError gopost_text_register_font(GopostEngine* engine,
                                      const char* family, const char* style_name,
                                      const uint8_t* data, size_t data_size) {
    if (!engine || !family) return GOPOST_ERROR_INVALID_ARGUMENT;
    (void)data;
    (void)data_size;
    std::lock_guard<std::mutex> lock(g_font_mutex);
    FontFamily ff;
    ff.family = family;
    ff.style_name = style_name ? style_name : "Normal";
    g_font_families.push_back(ff);
    return GOPOST_OK;
}

GopostError gopost_text_get_font_count(GopostEngine* engine, int32_t* out_count) {
    if (!engine || !out_count) return GOPOST_ERROR_INVALID_ARGUMENT;
    std::lock_guard<std::mutex> lock(g_font_mutex);
    *out_count = (int32_t)g_font_families.size();
    return GOPOST_OK;
}

GopostError gopost_text_get_font_family(GopostEngine* engine, int32_t index,
                                        char* out_family, int32_t buf_len) {
    if (!engine || !out_family || buf_len <= 0) return GOPOST_ERROR_INVALID_ARGUMENT;
    std::lock_guard<std::mutex> lock(g_font_mutex);
    if (index < 0 || index >= (int32_t)g_font_families.size())
        return GOPOST_ERROR_INVALID_ARGUMENT;
    strncpy(out_family, g_font_families[index].family.c_str(), (size_t)(buf_len - 1));
    out_family[buf_len - 1] = '\0';
    return GOPOST_OK;
}

GopostError gopost_text_rasterize(GopostEngine* engine,
                                  const GopostTextConfig* config,
                                  int32_t max_width,
                                  GopostFrame** out_frame) {
    if (!engine || !config || !out_frame) return GOPOST_ERROR_INVALID_ARGUMENT;
    if (!config->text) return GOPOST_ERROR_INVALID_ARGUMENT;

    int32_t w = 0, h = 0;
    get_text_dimensions(config, max_width, &w, &h);
    if (w <= 0 || h <= 0) {
        w = 1;
        h = 1;
    }

    size_t stride = (size_t)w * 4;
    size_t buf_size = stride * (size_t)h;

    GopostFrame* frame = static_cast<GopostFrame*>(malloc(sizeof(GopostFrame)));
    if (!frame) return GOPOST_ERROR_OUT_OF_MEMORY;

    frame->data = static_cast<uint8_t*>(calloc(1, buf_size));
    if (!frame->data) {
        free(frame);
        return GOPOST_ERROR_OUT_OF_MEMORY;
    }

    frame->width = (uint32_t)w;
    frame->height = (uint32_t)h;
    frame->format = GOPOST_PIXEL_FORMAT_RGBA8;
    frame->data_size = buf_size;
    frame->stride = stride;

    float scale = (config->font_size > 0.0f) ? config->font_size : 1.0f;
    float line_height_mult = (config->line_height > 0.0f) ? config->line_height : 1.0f;
    int glyph_w = (int)(kGlyphW * scale + 0.5f);
    int glyph_h = (int)(kGlyphH * scale + 0.5f);
    int line_h = (int)(glyph_h * line_height_mult + 0.5f);
    float letter_spacing = config->letter_spacing;

    int wrap_width = (max_width > 0) ? max_width : 65536;

    float cr = config->color.r, cg = config->color.g, cb = config->color.b, ca = config->color.a;
    float shadow_r = config->shadow_color.r, shadow_g = config->shadow_color.g;
    float shadow_b = config->shadow_color.b, shadow_a = config->shadow_color.a;
    int shadow_ox = (int)(config->shadow_offset_x + 0.5f);
    int shadow_oy = (int)(config->shadow_offset_y + 0.5f);
    float outline_r = config->outline_color.r, outline_g = config->outline_color.g;
    float outline_b = config->outline_color.b, outline_a = config->outline_color.a;
    int outline_w = (int)(config->outline_width + 0.5f);
    if (outline_w < 0) outline_w = 0;

    std::vector<int> line_widths;
    std::vector<std::string> lines;
    {
        std::string line;
        int line_w = 0;
        const char* p = config->text;
        while (*p) {
            unsigned char c = (unsigned char)*p++;
            if (c == '\n') {
                lines.push_back(line);
                line_widths.push_back(line_w);
                line.clear();
                line_w = 0;
                continue;
            }
            if (c < 32 || c > 126) c = 32;
            int gw = glyph_w + (int)letter_spacing;
            if (wrap_width > 0 && !line.empty() && (line_w + gw) > wrap_width) {
                lines.push_back(line);
                line_widths.push_back(line_w);
                line.clear();
                line_w = 0;
            }
            line += (char)c;
            line_w += gw;
        }
        if (!line.empty()) {
            lines.push_back(line);
            line_widths.push_back(line_w);
        }
    }

    int y = 0;
    for (size_t li = 0; li < lines.size(); li++) {
        const std::string& line = lines[li];
        int line_w = line_widths[li];
        int x;
        switch (config->alignment) {
            case GOPOST_TEXT_ALIGN_CENTER:
                x = (w - line_w) / 2;
                break;
            case GOPOST_TEXT_ALIGN_RIGHT:
                x = w - line_w;
                break;
            default:
                x = 0;
                break;
        }
        if (x < 0) x = 0;

        for (size_t i = 0; i < line.size(); i++) {
            unsigned char c = (unsigned char)line[i];
            if (c < 32 || c > 126) c = 32;
            int idx = c - 32;

            /* Draw outline (behind main glyph) */
            if (config->has_outline && outline_w > 0) {
                for (int oy = -outline_w; oy <= outline_w; oy++) {
                    for (int ox = -outline_w; ox <= outline_w; ox++) {
                        if (ox == 0 && oy == 0) continue;
                        draw_glyph(frame->data, w, h, stride,
                                   x + ox, y + oy, idx, scale,
                                   outline_r, outline_g, outline_b, outline_a);
                    }
                }
            }

            /* Draw shadow */
            if (config->has_shadow) {
                draw_glyph(frame->data, w, h, stride,
                           x + shadow_ox, y + shadow_oy, idx, scale,
                           shadow_r, shadow_g, shadow_b, shadow_a);
            }

            /* Draw main glyph */
            draw_glyph(frame->data, w, h, stride, x, y, idx, scale, cr, cg, cb, ca);

            x += glyph_w + (int)letter_spacing;
        }
        y += line_h;
    }

    *out_frame = frame;
    return GOPOST_OK;
}

GopostError gopost_canvas_add_text_layer(GopostCanvas* canvas,
                                         const GopostTextConfig* config,
                                         int32_t max_width,
                                         int32_t index,
                                         int32_t* out_layer_id) {
    if (!canvas || !config || !out_layer_id) return GOPOST_ERROR_INVALID_ARGUMENT;

    GopostEngine* engine = gopost_internal_canvas_get_engine(canvas);
    if (!engine) return GOPOST_ERROR_INVALID_ARGUMENT;

    GopostFrame* frame = nullptr;
    GopostError err = gopost_text_rasterize(engine, config, max_width, &frame);
    if (err != GOPOST_OK || !frame) return err;

    err = gopost_canvas_add_image_layer(canvas,
                                        frame->data,
                                        (int32_t)frame->width,
                                        (int32_t)frame->height,
                                        index, out_layer_id);

    free(frame->data);
    free(frame);

    if (err == GOPOST_OK) {
        gopost_layer_set_name(canvas, *out_layer_id, "Text");
    }
    return err;
}

GopostError gopost_canvas_update_text_layer(GopostCanvas* canvas,
                                            int32_t layer_id,
                                            const GopostTextConfig* config,
                                            int32_t max_width) {
    if (!canvas || !config) return GOPOST_ERROR_INVALID_ARGUMENT;

    GopostEngine* engine = gopost_internal_canvas_get_engine(canvas);
    if (!engine) return GOPOST_ERROR_INVALID_ARGUMENT;

    GopostFrame* frame = nullptr;
    GopostError err = gopost_text_rasterize(engine, config, max_width, &frame);
    if (err != GOPOST_OK || !frame) return err;

    err = gopost_internal_canvas_update_layer_pixels(canvas, layer_id,
                                                     frame->data,
                                                     (int32_t)frame->width,
                                                     (int32_t)frame->height);

    free(frame->data);
    free(frame);
    return err;
}

